class SpatialAttention(nn.Module):
    def __init__(self, in_channel):
        super(SpatialAttention, self).__init__()
        self.conv = nn.Conv2d(in_channel, in_channel, kernel_size=3, groups=in_channel, stride=1,
                              padding=1)  # 大核深度可分离卷积获取局部空间信息
        self.sigmod = nn.Sigmoid()

    def forward(self, x):
        y = x.clone()
        # 获取局部信息
        local_att = self.conv(y)  # size=c*h*w
        # 获取全局空间信息（位置在全局空间的占比）
        global_att = torch.mean(y, dim=1, keepdim=True)  # size=1*h*w
        # 该位置在空间中的占比与全局和局部重要性相关
        spatial_out = self.sigmod(local_att * global_att)
        out = spatial_out * x
        return out


class Dila_Conv(nn.Module):
    # Standard convolution
    def __init__(self, c1, c2, k=1, s=1, d=1, act=True):  # ch_in, ch_out, kernel, stride, padding, groups
        super(Dila_Conv, self).__init__()
        self.conv = nn.Conv2d(c1, c2, k, s, padding=d, dilation=d, bias=False)

        self.bn = nn.BatchNorm2d(c2)
        self.act = nn.SiLU() if act is True else (act if isinstance(act, nn.Module) else nn.Identity())
    def forward(self, x):
        return self.act(self.bn(self.conv(x)))
    def fuseforward(self, x):
        return self.act(self.conv(x))

class ESP(nn.Module):
    def __init__(self,c1,k=3,s=1):
        super(ESP, self).__init__()
        c=c1//4
        self.conv = Conv(c1, c1, 3, 1)
        self.conv1=Conv(c1,c,1,1)
        self.conv2=Conv(c1,c,1,1)
        self.conv3=Conv(c1,c,1,1)
        self.conv4=Conv(c1,c,1,1)
        self.conv_1=Dila_Conv(c,c,k,s,1)
        self.conv_2=Dila_Conv(c,c,k,s,2)
        self.conv_3=Dila_Conv(c,c,k,s,3)
        self.conv_4=Dila_Conv(c,c,k,s,4)
    def forward(self,x):

        stage_2=self.conv_1(self.conv1(x))

        stage_3= self.conv_2(self.conv2(x))+stage_2

        stage_4= self.conv_3(self.conv3(x))+stage_3

        stage_5= self.conv_4(self.conv4(x))+stage_4

        out=torch.cat([stage_2,stage_3,stage_4,stage_5],dim=1)

        return out+self.conv(x)


