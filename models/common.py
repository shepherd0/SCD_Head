class SA_Attention(nn.Module):
    def __init__(self, spatial_kernel=3):
        super(SA_Attention, self).__init__()
        # spatial attention
        self.conv = nn.Conv2d(1, 1, kernel_size=spatial_kernel, stride=1, padding=spatial_kernel // 2, bias=False)
        self.sigmod = nn.Sigmoid()

    def forward(self, x):
        max_out, _ = torch.max(x, dim=1, keepdim=True)
        avg_out = torch.mean(x, dim=1, keepdim=True)

        spatial_out = self.sigmod(self.conv(max_out + avg_out))

        spatial_out = x * spatial_out
        return x + spatial_out


