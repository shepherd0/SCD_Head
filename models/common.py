class DPE(nn.Module):
    def __init__(self, c1, c2):
        super().__init__()
        self.adjust_channel_forp1 = Conv(c1[0], c2, k=1)
        self.adjust_channel_forp2 = Conv(c1[1], c2, k=1)

        self.up_forp2 = nn.Sequential(
            nn.Upsample(scale_factor=2),
            Conv(c2, c2, k=1)
        )
        self.up_forp3 = nn.Sequential(
            nn.Upsample(scale_factor=2),
            Conv(c1[2], c2, k=1)
        )
        self.down = Conv(c2, c2, k=3, s=2)
        self.middle = Conv(c2, c2, k=1)

    def forward(self, x):
        x_p2 = self.adjust_channel_forp2(x[1])
        x_p1 = self.adjust_channel_forp1(x[0]) + self.up_forp2(x_p2)
        x_p1 = self.down(x_p1)

        x_p3 = self.up_forp3(x[2])

        return x_p1 + x_p2 + x_p3


class PatchMerging(nn.Module):
    r""" Patch Merging Layer.
    Args:
        input_resolution (tuple[int]): Resolution of input feature.
        dim (int): Number of input channels.
        norm_layer (nn.Module, optional): Normalization layer.  Default: nn.LayerNorm
    """

    def __init__(self, c1, norm_layer=nn.LayerNorm):
        super().__init__()

        self.dim = c1
        self.reduction = nn.Linear(4 * c1, 2 * c1, bias=False)
        self.norm = norm_layer(4 * c1)

    def forward(self, x):
        """
        x: B, H*W, C
        """
        B, C, H, W = x.shape
        x = x.permute(0, 2, 3, 1)  # x-> b,h,w,c
        x = x.view(B, H * W, -1)
        # B, L, C = x.shape
        # H = int(math.sqrt(L))
        # W = int(math.sqrt(L))
        L = H * W
        assert L == H * W, "input feature has wrong size"
        assert H % 2 == 0 and W % 2 == 0, f"x size ({H}*{W}) are not even."

        x = x.view(B, H, W, C)
        # 在行和列方向上间隔1选取元素
        x0 = x[:, 0::2, 0::2, :]  # B H/2 W/2 C
        x1 = x[:, 1::2, 0::2, :]  # B H/2 W/2 C
        x2 = x[:, 0::2, 1::2, :]  # B H/2 W/2 C
        x3 = x[:, 1::2, 1::2, :]  # B H/2 W/2 C

        # 拼接到一起作为一整个张量
        x = torch.cat([x0, x1, x2, x3], -1)  # B H/2 W/2 4*C
        x = x.permute(0, 3, 1, 2)  # b 4*c h/2 w/2

        x = x.view(B, -1, 4 * C)  # B H/2*W/2 4*C

        x = self.norm(x)  # 归一化操作
        x = self.reduction(x)  # 降维，通道降低2倍

        x = x.view(B, H // 2, W // 2, 2 * C)
        x = x.permute(0, 3, 1, 2)
        return x


