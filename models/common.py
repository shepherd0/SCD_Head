class Position_attention(nn.Module):
    def __init__(self, c1):
        super(Position_attention, self).__init__()
        self.conv = Conv(c1, c1, 1, 1)
        self.soft = nn.Softmax()

    def forward(self, x):
        y1 = self.conv(x)
        y2 = self.conv(x)

        y3 = torch.reshape(y1, (y1.shape[0], y1.shape[1], -1))

        y4 = torch.reshape(y2, (y2.shape[0], y2.shape[1], -1))

        y5 = y3.transpose(2, 1)

        y6 = torch.bmm(y5, y4)

        y7 = torch.reshape(y6, (y6.shape[0], y6.shape[1], x.shape[2], x.shape[3]))
        out = self.soft(torch.mean(y7, dim=1, keepdim=True))

        return out


